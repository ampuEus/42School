unsigned char	reverse_bits(unsigned char octet)
{
	unsigned int	len = 0;
	unsigned char	byte;

	while (len < 8)
	{
		byte = byte << 1;
		byte = (octet >> len & 1) | byte;
		len++;
	}
	return (byte);
}

